#include "libft.h"

char	*ft_skipis(const char *s, int (*f)(int))
{
	if (!s)
	{
		ft_error(WRONG_PARAMS);
		return (NULL);
	}
	while (*s && f(*s))
		++s;
	return ((char *)s);
}
