int		ft_atoi(const char *s)
{
	int a;
	int b;
	int c;

	a = 0;
	b = 1;
	c = 0;
	while (s[a] == '\n' || s[a] == '\t' || s[a] == '\v' ||
			s[a] == ' ' || s[a] == '\f' || s[a] == '\r')
		a++;
	if (s[a] == '-')
	{
		b = -1;
		a++;
	}
	else if (s[a] == '+')
		a++;
	while (s[a] >= '0' && s[a] <= '9')
	{
		c = s[a] - '0' + c * 10;
		a++;
	}
	return (b * c);
}
