char	*ft_strstr(const char *s1, const char *s2)
{
	int a;
	int b;
	int c;

	if (s1[0] == '\0' && s2[0] == '\0')
		return (&((char *)s1)[0]);
	a = 0;
	while (s1[a])
	{
		b = 0;
		c = a;
		while (s1[a] == s2[b] || !s2[b])
		{
			if (!s2[b])
				return (&((char *)s1)[c]);
			a++;
			b++;
		}
		a = c;
		a++;
	}
	return (0);
}
