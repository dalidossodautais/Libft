#include "libft.h"

void	ft_puttab(char **tab)
{
	int a;
	int b;

	a = 0;
	while (tab[a])
	{
		b = 0;
		while (tab[a][b])
			ft_putchar(tab[a][b++]);
		ft_putchar('\n');
		a++;
	}
}
