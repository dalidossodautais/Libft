char	*ft_strrchr(const char *s, int c)
{
	int a;

	a = 0;
	while (s[a])
		a++;
	while (a >= 0 && (int)s[a] != c)
		a--;
	if (a == -1)
		return (0);
	return ((char*)&s[a]);
}
