#include <string.h>

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t a;

	a = 0;
	if (s1 && s2)
	{
		while ((s1[a] || s2[a]) && a < n)
		{
			if (s1[a] != s2[a])
				return (0);
			a++;
		}
		return (1);
	}
	return (0);
}
